// Una tupla o lista es un tipo de dato como un vector pero con la
// diferencia de que puedes almacenar valores de distintos tipos de
// datos, por ejemplo, vamos a definir una tupla donde los valores sean
// el primero un string y el segundo un número:

let tupla: [string, number];

// Definimos las positiones 1 y 2. 
tupla[0] = "Hola"; 
tupla[1] = 3; 

// A partir de la posición 2 de la tupla aceptamos cualquiera 
// de los dos tipos definidos: string o número.

tupla[2] = 6; 

